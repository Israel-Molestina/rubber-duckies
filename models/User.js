const { Model, DataTypes } = require("sequelize");
const bcrypt = require("bcrypt");
const sequelize = require("../config/connection");

class User extends Model {
  checkPassword(loginPw) {
    return bcrypt.compareSync(loginPw, this.password);
  }
}

User.init(
  {
    userid: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
    },
    username: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    firstname: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    lastname: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    email: {
      type: DataTypes.STRING,
      allowNull: false,
      unique: true,
      validate: {
        isEmail: true,
      },
    },
    caregiverid: {
      type: DataTypes.INTEGER,
      references: {
        model: "caregiver",
        key: "caregiverid",
        unique: false,
      },
    },
    happy: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    sad: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    nervous: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    hungry: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    thirsty: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    restroom: {
      type: DataTypes.STRING,
      defaultValue: ""
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
      validate: {
        len: [8],
      },
    },
  },
  {
    hooks: {
      beforeCreate: async (newUserData) => {
        newUserData.password = await bcrypt.hash(newUserData.password, 10);
        return newUserData;
      },
      beforeUpdate: async (updatedUserData) => {
        updatedUserData.password = await bcrypt.hash(
          updatedUserData.password,
          10
        );
        return updatedUserData;
      },
    },
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: "user",
  }
);

module.exports = User;
